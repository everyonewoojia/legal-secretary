function generateResponse(userMessage, chatHistory) {
  const slots = extractSlots(userMessage)
  const aiMessages = chatHistory.filter((m) => m.role === 'agent' && !m.loading)
  const historyCount = aiMessages.length

  const flow = chatFlow.tech_service || chatFlow.default
  let reply = flow[Math.min(historyCount, flow.length - 1)] || chatFlow.default[flow.length - 1]

  const slotKeys = Object.keys(slots)
  if (slotKeys.length > 0) {
    const slotJson = JSON.stringify({ content: reply, slots })
    return { text: slotJson, hasSlots: true }
  }

  return { text: reply, hasSlots: false }
}

export function mockChatStream(sessionId, message, onChunk, onDone, onError, getMessages) {
  const messages = getMessages ? getMessages() : []
  let cancelled = false

  const run = async () => {
    try {
      await delay(200)

      const { text, hasSlots } = generateResponse(message, messages)

      const chars = text.split('')
      for (let i = 0; i < chars.length; i++) {
        if (cancelled) return
        await delay(20 + Math.random() * 30)
        onChunk(chars[i])
      }

      await delay(100)
      onDone?.()
    } catch (err) {
      onError?.(err.message || '模拟对话失败')
    }
  }

  run()

  return () => { cancelled = true }
}

