const slotExtractionMap = {
  甲方: /甲方[：:]\s*([^\n，,。.]+)/,
  乙方: /乙方[：:]\s*([^\n，,。.]+)/,
  合同金额: /(?:合同)?金额[：:]\s*([^\n，,。.]+)/,
  交付物: /交付物[：:]\s*([^\n，,。.]+)/,
  付款节点: /付款(?:节点|方式|条件)[：:]\s*([^\n，,。.]+)/,
  验收标准: /验收(?:标准|方式)[：:]\s*([^\n，,。.]+)/,
  违约责任: /违约(?:责任|条款)[：:]\s*([^\n，,。.]+)/,
  保密期限: /保密(?:期限|条款)[：:]\s*([^\n，,。.]+)/,
}

function extractSlots(text) {
  const slots = {}
  for (const [key, regex] of Object.entries(slotExtractionMap)) {
    const match = text.match(regex)
    if (match) {
      slots[key] = match[1].trim()
    }
  }
  return slots
}

